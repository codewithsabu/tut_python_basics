#arithmetic operators
# + operator to add two numbers
# - operator to subtract two numbers
# * operator to multiply two numbers
# / operator to divide two numbers
# // operator to perform integer division
# % operator to find the remainder of the division
# ** operator to calculate the power of a number

# program to demonstrate the use of arithmetic operators

# addition
first_number = int(input("Enter first number: "))
second_number = int(input("Enter second number: "))
sum = first_number + second_number
print("Sum:", sum)

# subtraction
difference = first_number - second_number
print("Difference:", difference)

# multiplication
product = first_number * second_number
print("Product:", product)

# division
quotient = first_number / second_number
print("Quotient:", quotient)

# integer division
integer_quotient = first_number // second_number
print("Integer Quotient:", integer_quotient)

# remainder
remainder = first_number % second_number
print("Remainder:", remainder)

# power
power = first_number ** second_number
print("Power:", power)
